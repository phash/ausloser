package de.germanwarfare.stats.entity;

public class Player {
	private String name;
	private int givenid;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getGivenid() {
		return givenid;
	}

	public void setGivenid(int givenid) {
		this.givenid = givenid;
	}

}
